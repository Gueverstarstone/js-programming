//trim() method
let name = 'jose se ph   '
let newName = name.trim()
console.log(newName);

//trimStart()
let greeting = "   Hello!   ";
console.log(greeting);  // "   Hello!   "
let trimmedStart = greeting.trimStart();
console.log(trimmedStart);  // "Hello!   "

//trimEnd()
let message = "   Wantam!   ";
console.log(message);  // "   Wantam!   "
let trimmedEnd = message.trimEnd();
console.log(trimmedEnd);  // "   Wantam!"